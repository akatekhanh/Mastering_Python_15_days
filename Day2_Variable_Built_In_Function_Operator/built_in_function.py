# Built in function

my_string = "Hello, world!"
print(len(my_string))


# Convert a integer into a string
my_number = 10
my_string = str(my_number)
print(my_string)   # Output: "10"

# Convert a string into integer
my_string = "10"
my_number = int(my_string)
print(my_number)   
# Output: 10


# Convert a string into a float
my_string = "10.5"
my_number = float(my_string)
print(my_number)   # Output: 10.5

